    private String CheckCivil(String val) {

        String c = "";

        if (val.equals("1")) {
            c = "Single";

        } else if (val.equals("2")) {
            c = "Married";
        } else if (val.equals("3")) {
            c = "Divorced";
        }
        return c;

    }
