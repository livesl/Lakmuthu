    private void SetCustID() {

        try {
            ResultSet rs = model.db.fetch("select max(id) from customer");
            txt_CusID.setText("0");
            if (rs.next()) {

                if (rs.getString(1) != null) {
                    int num = Integer.parseInt(rs.getString(1));
                    num++;
                    txt_CusID.setText("" + num);
                }


            }
            txt_FullName.grabFocus();


        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private String GetCity(String val, String prt) {
        String name = "";

        if (prt.equals("id")) { // return id
            try {

                ResultSet rs = model.db.fetch("SELECT\n"
                        + "city.id\n"
                        + "FROM\n"
                        + "city\n"
                        + "WHERE\n"
                        + "city.`name` = '" + val + "'");

                if (rs.next()) {
                    name = rs.getString(1);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else if (prt.equals("name")) { // return name


            try {

                ResultSet rs = model.db.fetch("SELECT\n"
                        + "city.`name`\n"
                        + "FROM\n"
                        + "city\n"
                        + "WHERE\n"
                        + "\n"
                        + "city.id = '" + val + "'");

                if (rs.next()) {
                    name = rs.getString(1);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }

        }

        return name;

    }

